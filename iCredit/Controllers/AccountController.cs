        [HttpPost]
        [AllowAnonymous]
       // [ValidateAntiForgeryToken]
        public ActionResult Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            //var result = await SignInManager.PasswordSignInAsync(model.Login, model.Password, model.RememberMe, shouldLockout: false);
            var result = validar(model);
            switch (result)
            {
                case SignInStatus.Success:
                    aspnetusers user=db.aspnetusers.Where(us=>us.UserName.ToUpper().Equals(model.Login.ToUpper())).FirstOrDefault();
                    usuario uxe = db.usuario.Where(u => u.aspnetusersId.Equals(user.Id) && u.Estado==true).FirstOrDefault();
                    if (uxe != null)
                    {
                        Session["EmpresaNombre"] = uxe.empresa.Nombre;
                        Session["EmpresaId"] = uxe.EmpresaId;
                        return RedirectToLocal(returnUrl);
                    }
                    else
                    {
                        ModelState.AddModelError("", "El usuario no se encuentra registrado en la empresa o esta inactivo.");
                        return View(model);
                    }
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }


        
